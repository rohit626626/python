#arithmetic
print(6+4) #print add 
print(6/4) #print divided output with float point
print(6//4) #print divided output without float point
print(6%4) #print reminder
print(2**4) #print 2 power 4

#assignment
x=5  #print 5
print(x)
x+=7  #print 12 (x=5 then x+=7 means 5+7=12)
print(x)

#comparison
i=5
print(i==5) #if i=5 print true
print(i!=5) #if i not equal to 5 print false
print(i>=5) #if i greater the equal to 5 print true
print(i<=5) #if i greater the equal to 5 print true

#logical
a=True
b=False
print(a and b) # a and b both true return true else false
print(a or b) # only one true return true else false

#identy
a=5
b=7
print(a is b) #a=b return true else false
print(a is not b) #a!=b return true else false

#membership 
l=[2,4,5,6,3,8]
print(4 in l) # 4 in list return true else false
print(4 not in l) #4 not in list return true else false

#bitwise
print(1&2) # work binary 1=01 and 2=10, 01 and 10 is 00 in binary return 0 in number
print(1|2) # work binary 1=01 and 2=10, 01 or 10 is 11 in binary return 3 in number
